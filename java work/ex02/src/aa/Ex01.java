package aa;

public class Ex01 {

	public static void main(String[] args) {
		System.out.println("익숙해지면 좋아요.");
		
		int a = 10;
		double b = 5.4;
		int c = 3;
		
		System.out.println(a + b);
		System.out.println(a / b);
		System.out.println(a % b);
		
		System.out.println(a / c);
		System.out.println(a % c);
		// 변수 선언 태그는 int(4bit), double(8bit), boolean(1bit)을 많이 씀
		// 8bit=1바이트
	}

}
